/**
 * 校验日期
 * 格式 yyyy-MM-dd 或 yyyy-M-d
 * 连字符可以没有或是“-”、“/”、“.”之一 
**/
export const dateReg = /^(?:(?!0000)[0-9]{4}([-/.]?)(?:(?:0?[1-9]|1[0-2])\1(?:0?[1-9]|1[0-9]|2[0-8])|(?:0?[13-9]|1[0-2])\1(?:29|30)|(?:0?[13578]|1[02])\1(?:31))|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)([-/.]?)0?2\2(?:29))$/

// 校验时间：时、分、秒必须是有效数字，如果数值不是两位数，十位需要补零。
export const timeReg = /^([0-1][0-9]|[2][0-3]):([0-5][0-9])$/

// 校验URL：支持 http、https、ftp、ftps
export const urlReg = /^(ht|f)(tp|tps)\:\/\/[a-zA-Z0-9\-\.]+\.([a-zA-Z]{2,3})?(\/\S*)?$/

/**
 * 校验邮箱
 * @符号前的邮箱用户和.符号前的域名(domain)必须满足以下条件：
 * > 字符只能是英文字母、数字、下划线_、.、- ；
 * > 首字符必须为字母或数字；
 * > _、.、- 不能连续出现。
**/
export const emailReg = /^[A-Za-z0-9](([_\.\-]?[a-zA-Z0-9]+)*)@([A-Za-z0-9]+)(([\.\-]?[a-zA-Z0-9]+)*)\.([A-Za-z]{2,})$/

//HTML标记
